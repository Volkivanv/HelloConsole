void FillArray(int[,] matr)
{
    for (int i = 0; i < matr.GetLength(0); i++)
    {
        for (int j = 0; j < matr.GetLength(1); j++)
        {
            matr[i, j] = new Random().Next(0, 100);
        }
    }
}

void PrintArray(int[,] matr)
{
    for (int i = 0; i < matr.GetLength(0); i++)
    {
        for (int j = 0; j < matr.GetLength(1); j++)
        {
            Console.Write($"{matr[i, j]} ");
        }
        Console.WriteLine();
    }
}

void PrintImage(int[,] matr)
{
    for (int i = 0; i < matr.GetLength(0); i++)
    {
        for (int j = 0; j < matr.GetLength(1); j++)
        {
            if (matr[i, j] == 1)
            {
                Console.Write($"{matr[i, j]} ");
            }
            else
            {
                Console.Write($"  ");
            }
        }
        Console.WriteLine();
    }
}

void FillImage(int[,] matr, int row, int column)
{
    if (matr[row, column] != 1)
    {
        matr[row, column] = 1;

        FillImage(matr, row + 1, column);
        FillImage(matr, row, column - 1);
        FillImage(matr, row - 1, column);
        FillImage(matr, row, column + 1);
    }
}

